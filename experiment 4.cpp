/* use of operational sign */
#include<stdio.h>
int main()
{
	int a=3,b=3,c=4,d=2,e=5,f=5,g=4,h=3,i=3,k=5 ;
	a+=b;
	c-=b;
	d*=b;
	e/=b;
	f%s=b;
	b<<=2;
	g>>=2;
	h&=2;
	i^=2;
	k|=2;
	printf ("result =%d\t%d\t%d\t%d\t%d\n",a,c,d,e,f);
	printf("result =%d\t%d\t%d\t%d\t%d\t\n",b,g,h,i,k);
	return 0;
	
}
